SELECT COUNT(*) COUNT, GENERATION 
FROM XX
WHERE ID NOT IN (SELECT DISTINCT PARENT_ID FROM XX WHERE PARENT_ID IS NOT NULL)
GROUP BY GENERATION 
ORDER BY GENERATION 